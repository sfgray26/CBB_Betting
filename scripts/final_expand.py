def expand_file(filepath, additional_rows, headers):
    """Expand a CSV file with additional rows."""
    existing = []
    with open(filepath, 'r', encoding='utf-8') as f:
        reader = csv.reader(f)
        file_headers = next(reader)
        for row in reader:
            existing.append(row)
    
    existing_names = set(row[0] for row in existing)
    
    with open(filepath, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(headers)
        for row in existing:
            writer.writerow(row)
        added = 0
        for row in additional_rows:
            if row[0] not in existing_names and row[0] != "":
                row[0] = normalize_name(row[0])
                writer.writerow(row)
                existing_names.add(row[0])
                added += 1
    
    return len(existing_names), added


